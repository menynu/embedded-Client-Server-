#ifndef LED_H_
#define LED_H_




/******************************************************************************
LED_init
Description: Initializes the LED GPIO (PA5) and its interrupt.
******************************************************************************/
void LED_init(void);

/******************************************************************************
LED_off
Description: Turns the LED off.
******************************************************************************/
void LED_off(void);

/******************************************************************************
LED_on
Description: Turns the LED on.
******************************************************************************/
void LED_on(void);

/******************************************************************************
LED_toggle
Description: Toggles the LED.
******************************************************************************/
void LED_toggle(void);

/******************************************************************************
LED_action
Description: this function will move the led state in this sequense:
 	 	 	 off, on, blinking (in half HZ) - in a circle.
******************************************************************************/
void LED_action();

#endif /* LED_H_ */
